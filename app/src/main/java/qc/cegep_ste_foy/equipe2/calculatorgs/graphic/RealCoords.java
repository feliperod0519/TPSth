package qc.cegep_ste_foy.equipe2.calculatorgs.graphic;

public class RealCoords {

    private float x = 0;
    private float y = 0;

    public RealCoords(float x, float y)
    {
        this.x = x;
        this.y = y;
    }

    public float getX()
    {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x)
    {
        this.x = x;
    }

    public void setY(float y)
    {
        this.y = y;
    }

}
